// insert first six termin info in DOM
function termine1() {
  console.log('load termine 1');
  // remove other termine
  if (currentTermine != 1) {
    $('.content').remove();
    $('.modal__info').remove();
  };

  for (let i = 0; i < 6; i++) {
    let termin = termine[i];
    $('#t' + (i + 1)).append(`<h4 class="content">${termin.name}</h4><div class="content"><p>${termin.datum}</p><p>${termin.uhrzeit}</p></div><h2 class="termin__more content">...</h2>`);
    $('#t__modal' + (i + 1)).append(`<div class="modal__info"><h4 class="content">${termin.name}</h4><div class="content"><p>${termin.datum}</p><p>${termin.uhrzeit}</p></div><p class="content">${termin.beschreibung}</p><p class="content">Preis: ${termin.preis}</p><p class="content">Anmeldung: ${termin.anmeldung}</p></div>`);
  }

  // set termine counter
  currentTermine = 1;
  //console.log(currentTermine);
};

