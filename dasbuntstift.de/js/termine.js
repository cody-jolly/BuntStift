// rotate termin blocks forward
function rotate() {
  console.log('toggle rotate');
  $('#t1').toggleClass('rotate');
  window.setTimeout(function(){$('#t2').toggleClass('rotate');}, 200);
  window.setTimeout(function(){$('#t3').toggleClass('rotate');}, 400);
  $('#t4').toggleClass('rotate');
  window.setTimeout(function(){$('#t5').toggleClass('rotate');}, 200);
  window.setTimeout(function(){$('#t6').toggleClass('rotate');}, 400);
};

// rotate termin blocks back
function rotateBack() {
  console.log('toggle rotate back')
  $('#t3').toggleClass('rotate-back');
  window.setTimeout(function(){$('#t2').toggleClass('rotate-back');}, 200);
  window.setTimeout(function(){$('#t1').toggleClass('rotate-back');}, 400);
  $('#t6').toggleClass('rotate-back');
  window.setTimeout(function(){$('#t5').toggleClass('rotate-back');}, 200);
  window.setTimeout(function(){$('#t4').toggleClass('rotate-back');}, 400);
};

// add event handlers to forward and back arrows
$('#termine__back').click(function(){
  console.log('back');
  if (currentTermine === 2) {
    window.setTimeout(termine1, 800);
  }
  rotateBack();
  window.setTimeout(rotateBack, 1000);
});

$('#termine__forward').click(function(){
  console.log('forward');
  if (currentTermine === 1) {
    window.setTimeout(termine2, 800);
  }
  rotate();
  window.setTimeout(rotate, 1000);
});

